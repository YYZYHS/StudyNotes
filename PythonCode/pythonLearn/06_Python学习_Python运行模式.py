# =============== Python运行模式 ===============

# 1. 命令行模式（脚本模式）
"""
- 将代码保存为.py文件（例如hello.py）
- 终端执行：python hello.py
- 适合完整程序开发
- 程序会从顶部开始顺序执行
- 可以通过sys.argv接收命令行参数
"""

# 2. 交互模式（REPL模式）
"""
- 终端输入python进入
- 即时执行单行代码，适合测试
- 支持tab补全、历史命令
- 使用_获取上次运算结果
- 退出：quit()或Ctrl+D
"""

# 3. IDE模式（如PyCharm）
"""
- 结合编辑器、调试器等功能
- 可以交互式执行选中的代码块
- 提供高级调试功能
"""

# 使用建议：
"""
1. 学习阶段多用交互模式做快速测试
2. 开发实际项目用脚本模式
3. IDE适合大中型项目开发
"""


# 示例：测试不同模式
def test_modes():
    print("这是一个测试函数")


if __name__ == "__main__":
    test_modes()
    # 在命令行模式执行时会运行
    # 在交互模式需要手动调用test_modes()

